# Sintaxe Python
# Podemos executar escrevendo diretamente na linha de comando ou criando um arquivo Python
# no servidor usando a extensão .py
print("Hello, World!")

# Recuo Python
# Refere-se ao espaço deixado no inicio de um linha de código
# Python usa indentação para indicar um bloco de código.
if 5 > 6:
    print("É maior")
# O numero de espaço depende exclusivamente do programador, mas deve ser obrigatoriamento
# pelo menos um.
# Cada instrução que esta no mesmo nivel no bloco deve ter o mesmo numero de espaço
# Caso tenha um bloco dentro de outro bloco deve ter o numero de espaço maior que o bloco anterior.

