# Operadores de comparação
# Operadores lógicos
# Operadores de identidade
# Operadores de associação
# Operadores bit a bit

#Operadores Aritméticos
# Adição (+)
# Subtração (-)
# Multiplicação (*)
# Divisão (/)
# Modulo (%)
# Exponenciação (**)
