# Operador de Atribuição
# Atribuição (=)
# Soma dele mesmo com n (+=)
# Subtração dele mesmo com n (-=)
# Multiplicação dele mesmo com n (*=)
# Divisão dele mesmo com n (/=)
# Modulo dele mesmo com n (%=)
# Divisão inteira(//=)
# Exponeciação (**=)
# (&=)
# (|=)
# (^=)
# (>>=)
# (<<=)

# Operador de Comparação
# igual ==
# Não igual !=
# Maior que >
# Menor que <
# Maior ou igual >=
# Menor ou igual <=

#Operadores Lógicos
# and retorna booelano se as duas sentenças estiver de acordo.
# or retorna booleano se uma das duas sentenças estiver de acordo.
# not retorna o valor inverso ao verdadeiro

# Operadores de Identidades
# Operadores de identidades são usados para comprar os objetos. Se eles forem realmente
# o mesmo objeto, com o mesmo local de memoria
# is retorna booleano
# is not retorna booleano invertido

# Operadores de Associação 
# São operadores usados para testar se um sequencia é apresentada em um obejto
# in 	Returns True if a sequence with the specified value is present in the object
# not in Returns True if a sequence with the specified value is not present in the object

# Operadores bit a bit (Operadores logicos em Sistemas Digitais)
# São utilizados para comprar numeros binarios
# & 	AND	Sets each bit to 1 if both bits are 1
# |	    OR	Sets each bit to 1 if one of two bits is 1
#  ^	XOR	Sets each bit to 1 if only one of two bits is 1
# ~ 	NOT	Inverts all the bits
# <<	Zero fill left shift	Shift left by pushing zeros in from the right and let the leftmost bits fall off
# >>	Signed right shift	Shift right by pushing copies of the leftmost bit in from the left, and let the rightmost bits fall off

