// Ser mais curta
// Ter o this associada a função escrita

// Função Normal
let dobro = function( a){
    return  2 * a;
}
// Arrow function
dobro = (a) => {
    return 2 * a;
}
// Arrow function reduzida
dobro = a => 2 * a;

let ola = function(){
    return 'ola';
}
ola = () => 'ola';
// Função com unico parametro #2
ola = _ => 'ola';