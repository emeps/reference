// Os Operadores Aritmeticos são:
// Adição que pode ser feita por numeros inteiros, numeros fluantes.
console.log(10 + 10);
console.log(10.5 + 10.6);
// Subtração que pode ser feita por numeros inteiros e numeros flutuantes
console.log (10-1);
console.log(10.6 - 20);
// Multiplicação que pode ser feita por numeros inteiros e numeros flutuantes
console.log(10*9);
// Divisão que pode ser feita por numeros inteiros e numeros flutuantes
console.log(10/5);
// Resto que pode ser feita por numeros inteiros. 
console.log((10%2));
// Potenciação
console.log(10**2);
// Ou
console.log(Math.pow(10,2));

